namespace TS4Tools.Core.System.Platform;

/// <summary>
/// Represents the different platform types supported by TS4Tools.
/// </summary>
public enum PlatformType
{
    /// <summary>
    /// Windows operating system.
    /// </summary>
    Windows,
    
    /// <summary>
    /// macOS operating system.
    /// </summary>
    MacOS,
    
    /// <summary>
    /// Linux operating system.
    /// </summary>
    Linux,
    
    /// <summary>
    /// Unknown or unsupported operating system.
    /// </summary>
    Unknown
}
